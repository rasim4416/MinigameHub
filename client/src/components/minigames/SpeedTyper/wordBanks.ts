// ─── Helper functions ─────────────────────────────────────────────────────────

export const getRandomWord = (lang: Language): string => {
  const words = wordBanks[lang];
  return words[Math.floor(Math.random() * words.length)];
};

export const getWordOfLength = (lang: Language, length: number): string => {
  const map = byLength[lang];
  if (map.has(length)) {
    const arr = map.get(length)!;
    return arr[Math.floor(Math.random() * arr.length)];
  }
  // Find nearest available length
  const lengths = [...map.keys()];
  lengths.sort((a, b) => Math.abs(a - length) - Math.abs(b - length));
  const nearest = map.get(lengths[0])!;
  return nearest[Math.floor(Math.random() * nearest.length)];
};

export const getWordInRange = (lang: Language, min: number, max: number): string => {
  const words = wordBanks[lang].filter(w => w.length >= min && w.length <= max);
  if (words.length === 0) return getRandomWord(lang);
  return words[Math.floor(Math.random() * words.length)];
};

export const getLetterCountForLevel = (level: number): number => Math.min(level + 2, 9);

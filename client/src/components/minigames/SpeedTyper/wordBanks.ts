// Per-length lookup maps (built once at module load)
const buildLengthMap = (words: string[]): Map<number, string[]> => {
  const map = new Map<number, string[]>();
  for (const w of words) {
    const len = w.length;
    if (!map.has(len)) map.set(len, []);
    map.get(len)!.push(w);
  }
  return map;
};

const englishByLength = buildLengthMap(wordBanks.english);
const turkishByLength = buildLengthMap(wordBanks.türkçe);

